/*
    Bool Expressions
        - expressions vs statements
            - expressions are some code that runs and returns a value. Generally they are horizontal
                - var declaration, func expression, boolean expression
            - statements don't return output, they happen vertically
                - for, if, func statement
        - bool types
            - true, false
        - operators
            - not 
                - !
            - or
                - ||
            - and
                - &&
*/

/*
    Roughly, a statement performs an action. Loops and if statements are examples of statements. A program 
    is basically a sequence of statements (we're ignoring declarations here). Where JavaScript expects a 
    statement, you can also write an expression. Such a statement is called an expression statement. The 
    reverse does not hold: you cannot write a statement where JavaScript expects an expression. For example,
    an if statement cannot become the argument of a function.
*/